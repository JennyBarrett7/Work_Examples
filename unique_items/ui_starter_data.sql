# starter data for unique_items database

CREATE DATABASE IF NOT EXISTS unique_items;

USE unique_items;


/*Table structure for table `customers` */

DROP TABLE IF EXISTS customers;

CREATE TABLE IF NOT EXISTS customers 
(
id INT AUTO_INCREMENT PRIMARY KEY,
fname VARCHAR(50) NOT NULL DEFAULT "Missing",
lname VARCHAR(50) NOT NULL DEFAULT "Missing",
email VARCHAR(255) NOT NULL DEFAULT "Missing",
address_line1 VARCHAR(100) NOT NULL DEFAULT "Missing",
address_line2 VARCHAR(100) DEFAULT NULL,
city VARCHAR(50) NOT NULL DEFAULT "Missing",
state VARCHAR(50) NOT NULL DEFAULT "Missing",
zipcode CHAR(5) NOT NULL DEFAULT "Empty"	
);


/*Table structure for table `products` */

DROP TABLE IF EXISTS products;

CREATE TABLE IF NOT EXISTS products
(
id INT AUTO_INCREMENT PRIMARY KEY,
category VARCHAR(100) NOT NULL DEFAULT "Missing",
subcategory VARCHAR(100) NOT NULL DEFAULT "Missing",
name VARCHAR(100) NOT NULL DEFAULT "Missing",
price DECIMAL(6,2) NOT NULL DEFAULT 0
);


/*Table structure for table `orders` */

DROP TABLE IF EXISTS orders;

CREATE TABLE IF NOT EXISTS orders 
(
  id INT AUTO_INCREMENT PRIMARY KEY,
  order_date DATE,
  cust_id INT NOT NULL,
  paid BOOLEAN,
  CONSTRAINT fk_cust_id
  FOREIGN KEY(cust_id) REFERENCES customers(id)
    ON UPDATE CASCADE
    ON DELETE RESTRICT
);


/*Table structure for table `order_items` */

DROP TABLE IF EXISTS order_items;

CREATE TABLE IF NOT EXISTS order_items
(
order_id INT NOT NULL,
product_id INT NOT NULL,
quantity_ordered INT NOT NULL,
CONSTRAINT fk_product_id
FOREIGN KEY(order_id) REFERENCES orders(id),
FOREIGN KEY(product_id) REFERENCES products(id)
  ON UPDATE CASCADE
  ON DELETE RESTRICT
);


/*view structure for view `amount_per_order` */

DROP VIEW IF EXISTS amount_per_order;

CREATE VIEW amount_per_order AS
SELECT 
order_id, 
SUM(quantity_ordered * price) Total
FROM order_items
INNER JOIN products 
ON order_items.product_id = products.id
GROUP by order_id
ORDER BY total DESC;


